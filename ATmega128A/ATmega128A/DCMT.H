#ifndef DCMT_H_
#define DCMT_H_

#define CW 1
#define CCW 0

#define IN1_H		BH(PORTA, 0)
#define IN1_L		BL(PORTA, 0)
#define IN2_H		BH(PORTA, 1)
#define IN2_L		BL(PORTA, 1)


/*----------------------------------------//
				Stop the Motor
//----------------------------------------*/

void MT_Stop(void)
{
	
	IN1_L;
	IN2_L;
	
}


/*----------------------------------------//
			Set the Wise of Motor
//----------------------------------------*/

void MT_Wise(unsigned int x)
{
	
	if (x == CW)
	{
		IN1_H;
		IN2_L;
	}
	
	if (x == CCW)
	{
		IN1_L;
		IN2_H;
	}
	
}

#endif /* DCMT_H_ */